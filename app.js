document.getElementById('language').addEventListener('change', (event) => {
    const selectedLang = event.target.value;
    const ul = document.getElementById('points');
    ul.innerHTML = "";
    points[selectedLang].forEach(point => {
        const li = document.createElement('li');
        li.textContent = point;
        ul.appendChild(li);
    });
});

function readSummary() {
    const selectedLang = document.getElementById('language').value;
    const msg = new SpeechSynthesisUtterance(points[selectedLang].join('. '));
    msg.lang = selectedLang === 'en' ? 'en-US' :
               selectedLang === 'hi' ? 'hi-IN' :
               selectedLang === 'pa' ? 'pa-IN' :
               selectedLang === 'mr' ? 'mr-IN' : 'en-US'; // Adjust language codes
    window.speechSynthesis.speak(msg);
}
